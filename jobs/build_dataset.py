# Standard library imports
import json
import logging
import os
from pathlib import Path

# Third-party imports
import numpy as np
import pandas as pd


def load_raw_companies(raw_companies_fpath: Path) -> pd.DataFrame:
    """Loads and cleans a JSON file of raw company data.

    Args:
        raw_companies_fpath: The path to a raw dataset of
            publicly-traded companies. Generated by the IDI
            company info processor (i.e., the LSEG data pipeline).

    Returns:
        The dataset as a Pandas DataFrame.
    """
    # Load JSON file of companies
    try:
        companies_df = pd.read_json(raw_companies_fpath)
    except FileNotFoundError as e:
        raise RuntimeError(
            "The companies data file was not found at the given path."
        ) from e
    except json.JSONDecodeError as e:
        raise RuntimeError(
            "The companies data file could not be parsed as JSON."
        ) from e

    # Drop ununsed columns
    companies_df = companies_df.drop(
        columns=["fax_number", "phone_number", "original_entity_name", "last_processed"]
    )

    # Drop rows with duplicate PermIDs that are also missing addresses
    is_dupe = companies_df.duplicated(subset=["permid_id"], keep=False)
    missing_addr = (
        companies_df["incorporated_in"].isnull() | companies_df["domiciled_in"].isnull()
    )
    companies_df = companies_df[~(is_dupe & missing_addr)]

    # Otherwise, keep only first row upon encountering a set of dupes with all values in common
    companies_df = companies_df.drop_duplicates()

    # Standardize ticker column
    companies_df.loc[:, "identifier"] = companies_df["identifier"].apply(
        lambda id: id.split("ticker:")[-1]
    )

    # Group rows by PermID; aggregate ticker values into list, take first value of other columns
    col_ops = {"identifier": list} | {
        col: "first"
        for col in companies_df.columns
        if col not in ("identifier", "permid_id")
    }
    companies_df = companies_df.groupby("permid_id").agg(col_ops).reset_index()

    # Finalize columns for company dataset
    companies_df["permId"] = companies_df["permid_id"].astype(str)
    companies_df["name"] = companies_df["investor_name"]
    companies_df["country"] = companies_df["incorporated_in"]
    companies_df["tickers"] = companies_df["identifier"]
    companies_df = companies_df[["permId", "lei", "name", "country", "tickers", "url"]]

    # Replace NaN values
    companies_df = companies_df.replace({np.nan: ""})

    return companies_df


def load_country_codes(country_codes_fpath: Path) -> dict:
    """Loads a JSON file of country name-country code mappings.

    Args:
        country_codes_fpath: The file path for a mapping with
            country names as keys and country codes as values.

    Returns:
        The mapping.
    """
    try:
        with open(country_codes_fpath, encoding="utf-8") as f:
            return json.load(f)
    except FileNotFoundError as e:
        raise RuntimeError(
            "The country codes mapping file was not found at the given path."
        ) from e
    except (pd.errors.ParserError, json.JSONDecodeError) as e:
        raise RuntimeError(
            "The country codes mapping file could not be parsed as JSON."
        ) from e


def load_company_extras(company_extras_fpath: Path) -> pd.DataFrame:
    """Loads a dataset of additional company metadata.

    The dataset was generated by Claude Sonnet and contains "permId",
    "name", "sectors", and "subsidiaries" columns, with a sizable
    number of blank or null values.

    Args:
        company_extras_fpath: The path to a dataset of additional
            company metadata.

    Returns:
        The dataset as a Pandas DataFrame.
    """
    # Load TSV file of additional company information
    try:
        company_extras_df = pd.read_csv(
            company_extras_fpath,
            delimiter="\t",
            header=None,
            names=["permId", "name", "sectors", "subsidiaries"],
            dtype=str,
        )
    except FileNotFoundError as e:
        raise RuntimeError(
            "The company extras file was not found at the given path."
        ) from e
    except pd.errors.ParserError as e:
        raise RuntimeError(
            "The company extras file could not be parsed as a CSV file."
        ) from e

    # Parse delimited columns into list columns
    for col in ("sectors", "subsidiaries"):
        company_extras_df[col] = company_extras_df[col].apply(
            lambda val: [] if val == "—" else val.split(", ")
        )

    return company_extras_df


def main(logger: logging.Logger) -> None:
    """Builds a dataset of companies for the downstream web application.

    Args:
        logger: A standard logger instance.

    Raises:
        `RuntimeError` if any required environment variables is missing
            or if the script fails due to a missing file or data
            parsing error.

    Returns:
        `None`
    """
    # Parse environment variables
    logger.info("Parsing environment variables.")
    try:
        raw_companies_fpath = os.environ["RAW_COMPANIES_FILE_PATH"]
        country_codes_fpath = os.environ["COUNTRY_CODES_FILE_PATH"]
        company_extras_fpath = os.environ["COMPANY_EXTRAS_FILE_PATH"]
        output_fpath = os.environ["OUTPUT_FILE_PATH"]
    except KeyError as e:
        raise RuntimeError(f'Missing required environment variable "{e}".') from e

    # Load datasets
    logger.info("Loading raw company dataset.")
    companies_df = load_raw_companies(raw_companies_fpath)

    logger.info("Loading country name-code mappings.")
    country_codes_map = load_country_codes(country_codes_fpath)

    logger.info("Loading company extras TSV file.")
    company_extras_df = load_company_extras(company_extras_fpath)

    # Add country codes to companies
    logger.info("Mapping country names to country codes.")
    companies_df["countryCode"] = companies_df["country"].map(country_codes_map)

    # Merge companies data with extra fields
    logger.info("Merging base company data with extras.")
    merged_df = companies_df.merge(
        company_extras_df[["permId", "sectors", "subsidiaries"]],
        how="left",
        on="permId",
    )

    # Fill NaN values
    logger.info("Filling NaN values")
    merged_df.loc[:, "sectors"] = merged_df["sectors"].apply(
        lambda sector: sector if sector is not np.nan else []
    )
    merged_df.loc[:, "subsidiaries"] = merged_df["subsidiaries"].apply(
        lambda sub: sub if sub is not np.nan else []
    )
    merged_df = merged_df.replace({np.nan: ""})

    # Write dataset as JSON output file
    # NOTE: Pandas includes escape characters in its JSON output;
    # to avoid this, the dataset is parsed using the JSON library
    # before it is written to disk.
    logger.info("Writing dataset to output JSON file.")
    json_str = merged_df.to_json(orient="records")
    parsed = json.loads(json_str)
    with open(output_fpath, "w", encoding="utf-8") as f:
        json.dump(parsed, f, ensure_ascii=False, indent=2)

    # Log success
    logger.info("Pipeline completed successfully.")


if __name__ == "__main__":
    logging.basicConfig(
        level=logging.INFO, format="%(name)s - %(levelname)s - %(message)s"
    )
    logger = logging.getLogger("BUILD DATASET")
    try:
        main(logger)
    except Exception as e:
        logger.error(f"An unexpected error occured: {e}.")
        exit(1)
